#escape characters
print("HELLO WORLD")
#\t for a tab space
print("Hello\tworld")
#\n for new line
print("hello\nworld")
#end="\n" for new line(u can give more than 1 times)
print("hello world",end="\n")
print("hello world")
#end="\t" for a tab space(u can give more than 1 times)
print("hello world",end="\t\t")
print("hello world")
#end="" keeps on same line
print("hello world",end="")
print("pakistan")
#\b removes the single character or backspace
print("Hello \bWorld!")